    private void open(File file) {

        ProjectObject temp = null;

        try (ObjectInputStream in = new ObjectInputStream(new FileInputStream(file))) {
            temp = (ProjectObject) in.readObject();

        } catch (Exception ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (temp == null) {
            return;
        }

        configureNewProject(temp);
        setDefaultButton(temp.getDefaultValue());

        JButton btn = createButton(defaultValue);
        btn.setName("defaultButton");
        pnDefault.add(btn);

        Map<String, Set<GridValue>> tg = new LinkedHashMap<>(temp.getValues().size());

        //Load tabs
        for (GridValue gv : temp.getValues()) {

            if (temp.getDefaultValue().equals(gv)) {
                continue;
            }

            if (!tg.containsKey(gv.getTabName())) {
                tg.put(gv.getTabName(), new LinkedHashSet<GridValue>(20));
            }

            tg.get(gv.getTabName()).add(gv);
        }

        for (String k : tg.keySet()) {
            Set<GridValue> gv = tg.get(k);

            if (TabNames.COLORS.getName().equals(k)) {
                add(gv, TabNames.COLORS, ButtonType.COLOR);

            } else if (TabNames.TILES.getName().equals(k)) {
                add(gv, TabNames.TILES, ButtonType.IMAGE);

            } else {
                addPlainValues(gv);
            }

        }

        gridValuesList.addAll(temp.getGrids());

        temp.getValues().clear();
        temp.getGrids().clear();

    }

    private void save() {
        po.setDefaultValue(defaultValue);
        po.setGrids(gridValuesList);

        for (JPanel tab : getPanels(tabValues)) {
            for (JButton btn : getButtons(tab)) {

                if ("defaultButton".equals(btn.getName())) {
                    continue;
                }

                GridValue gv = createNewValue((JButton) btn);
                gv.setTabName(tab.getName());
                po.getValues().add(gv);
            }
        }

        try (ObjectOutputStream out = new ObjectOutputStream(new FileOutputStream(po.getName() + ".ame"))) {
            out.writeObject(po);
        } catch (IOException ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

