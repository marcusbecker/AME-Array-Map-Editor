    private int generateId(JButton btn) {
        if (defaultValue.getPlain().equals(getText(btn))) {
            return -1;
        }

        String temp = (btn.getText() != null ? btn.getText() : "") + (btn.getName() != null ? btn.getName() : "");
        return temp.hashCode();
    }

    private GridValue createNewValue(JButton btn) {
        int id = generateId(btn);

        //System.out.println("id = " + id);
        GridValue g = new GridValue(id, getText(btn), getImage(btn));

        if (ButtonType.COLOR.name().equals(btn.getName())) {
            g.setColor(btn.getBackground());
        }

        return g;
    }

    private boolean isSameButton(JButton btn, GridValue value) {
        return generateId(btn) == value.getId();
    }

    private void updateMousePosition() {
        Dimension grid = po.getGrid();

        if (grid == null || grid.width == 0 || grid.height == 0) {
            return;
        }

        int px = pxy.x / (size.x / grid.width);
        int py = pxy.y / (size.y / grid.height);

        if (px >= grid.width || py >= grid.height) {
            lblPosition.setForeground(Color.RED);
        } else {
            lblPosition.setForeground(Color.BLACK);
        }

        lblPosition.setText(String.format("lin %d, col %d", py, px));
    }

    private boolean isValid(int px, int py, Object[][] gridArray) {
        return px >= 0 && px < gridArray.length && py >= 0 && py < gridArray[0].length;
    }

